public class TerceiroExercicico {
    public static void main(String args[]) {
        int numero1 = 20;
        int numero2 = 50;
        int soma = numero1 + numero2;
        double divisor = numero1 / numero2;
        int multiplication = numero1 * numero2;
        int subtract = numero1 - numero2;

        System.out.println("A soma das vriaveis é: " + soma);
        System.out.println("A multiplicação das vriaveis é: " + multiplication);
        System.out.println("A divisão das vriaveis é: " + divisor);
        System.out.println("A subtração das vriaveis é: " + subtract);

    }
}
